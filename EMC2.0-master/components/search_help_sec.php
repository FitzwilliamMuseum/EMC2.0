    <h1> Search help </h1>

    <h3> Regular search </h3>

    <p> Use a free text query in order to retrive
        records, these should load up without the page having to refresh, clicking on the image or the "full record" link at the bottom of the card will take you to a details page for that particular record. </p>

    <h3> Advanced search </h3>

    <p> Advanced search allows you to perform multiple queries at the same time, based on several criteria, for example
        you could find all coins containing the word Edward and Redwulf, between a certain time period. All records are presented in chronological order. </p>

    <h3> Please note the following: </h3>
    <h4> EMC/SCBI numbers: </h4>
    <p> EMC record numbers are derived from the year in which they were found or recorded, followed by the number in sequence for that year. Thus 2012.0123 is the 123rd coin recorded in 2012.</p>
    <p> SCBI record numbers are derived from the numbers assigned in published SCBI volumes. The first two digits of all SCBI records are always 10. Thus 1001.0321 is coin number 321 in SCBI volume 1.</p>
    <h4> Mapping: </h4>
    <p>Two forms of map are available based on EMC/SCBI records.</p>
    <p>A heatmap visualises concentrations of finds, with greater density producing a higher (brighter) level of ‘heat’. Note that the pattern may shift depending on level of magnification.
   </p>
   <p>A marker map shows points and labels for all locations where relevant coins have been found. Note that some locations are approximate, and some have produced a substantial number of finds. Also, full spatial data on finds held by the project is not released to the public, in order to protect the confidentiality of finders and landowners. Find-spots are normally only divulged to the level of parish. The placement of records within each parish area does not reflect the location of a coin-find.</p>
    <h4> Find-spots: </h4>

    <p> Find-spots are given only to parish level. In some instances the name of the parish in which a coin was found
        may also be restricted, or unknown, in which case only the unitary authority or region of discovery (e.g.
        'East-Anglia') is given. </p>
