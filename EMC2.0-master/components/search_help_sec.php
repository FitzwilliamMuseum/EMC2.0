    <h4> Names of moneyers </h4>

    <p> Moneyers were people responsible for minting, who placed their names on coins in the early Middle Ages. They
        used a multitude of spelings, even for the same individual. For the benefit of analysis, these are generally
        normalised to a late West Saxon form of Old English, or to the standard form of Old Norse for names of
        Scandinvaian derivation, as listed in SCBI vols. 28 and 41. These names and their variants are too numerous to
        list here, but note that Advanced Search offers the option of searching by the name as inscribed on the
        coin.</p>
    <h4> Type </h4>

    <p> Names of types used in the database are listed in roughly chronological order. Early English gold coins follow
        C. H. V. Sutherland, <i> Anglo-Saxon Gold Coinage in the Light of the Cronall Hoard </i> (Oxford, 1948); early
        silver pennies follow D. M. Metcalf, <i>Thrymsas and Sceattas in the Ashmolean Museum, Oxford, </i> 3 vols.
        (London, 1993-4); and subsequent coins follow J. J. North, <i>English Hammered Coinage, vol. I: Early
            Anglo-Saxon to Henry III, c. AD 600-1272,</i> 3rd ed. (London, 1994).
    
